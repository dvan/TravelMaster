package route;

public class Line {

	private int number;
	private String type;
	
	
	public void setNumber(int number){
		number = this.number;
	}
	
	public int getNumber(){
		return number;
	}
	
	public void setType(String type){
		type = this.type;
	}
	
	public String getType(){
		return type;
	}
}
